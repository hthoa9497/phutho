use Drupal\author_management\AuthorManagementInterface;

/**
 * Defines the author management entity class.
 *
 * @ContentEntityType(
 *   id = "author_management",
 *   label = @Translation("Author Management"),
 *   label_collection = @Translation("Author Managements"),
 *   handlers = {
 *     "view_builder" = "Drupal\Core\Entity\EntityViewBuilder",
 *     "list_builder" = "Drupal\author_management\AuthorManagementListBuilder",
 *     "views_data" = "Drupal\views\EntityViewsData",
 *     "form" = {
 *       "add" = "Drupal\author_management\Form\AuthorManagementForm",
 *       "edit" = "Drupal\author_management\Form\AuthorManagementForm",
 *       "delete" = "Drupal\Core\Entity\ContentEntityDeleteForm"
 *     },
 *     "route_provider" = {
 *       "html" = "Drupal\Core\Entity\Routing\AdminHtmlRouteProvider",
 *     }
 *   },
 *   base_table = "author_management",
 *   admin_permission = "administer author management",
 *   entity_keys = {
 *     "id" = "id",
 *     "label" = "title",
